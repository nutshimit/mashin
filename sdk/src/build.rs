use std::path::Path;

pub fn build() {
	if let Ok(target) = std::env::var("CARGO_MANIFEST_DIR") {
		println!("cargo:rustc-env=TARGET={}", target);
		println!(
			"cargo:rerun-if-changed={}",
			Path::new(&target).join("bindings.json").to_str().expect("valid path")
		);
	}
}
